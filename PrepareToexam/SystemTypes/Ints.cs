        // Aliase in C# is int.
        // Size Signed 32-bit integer.
        // Range from -2,147,483,648 to 2,147,483,647.
        // Default value is 0.
        int intValue1 = 90_946;                               // 90946
        int intValue2 = 0x0001_6342;                          // 90946 
        int intValue3 = 0b0001_0110_0011_0100_0010;           // 90946
     // int intValue4 = 0x_0001_6342;                            90946    C# 7.2 onwards  
     // int intValue5 = 0b_0001_0110_0011_0100_0010;             90946    C# 7.2 onwards 

    }
}
